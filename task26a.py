# AND and OR operation logic
variable1 = True
variable2 = False
variable3 = False
variable4 = False
result = variable1 or variable2 or variable3 or variable4
print(result)
# if there is atleast one operator as True then the "result = variable1 or variable2 or variable3 or variable4" becomes True
# and if the operator are all False then it will be false
